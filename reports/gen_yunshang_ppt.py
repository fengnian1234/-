def bg(s, c=NW): f=s.background.fill; f.solid(); f.fore_color.rgb=c

def tb(s,l,t,w,h,tx,sz=14,c=DK,bd=False,al=PP_ALIGN.LEFT):
    """创建文本框。注意：h 必须足够容纳文字，避免溢出遮挡。"""
    bx=s.shapes.add_textbox(Cm(l),Cm(t),Cm(w),Cm(h))
    tf=bx.text_frame; tf.word_wrap=True; tf.auto_size=None
    p=tf.paragraphs[0]; p.text=tx; p.font.size=Pt(sz)
    p.font.color.rgb=c; p.font.bold=bd; p.alignment=al
    return tf

def rect(s,l,t,w,h,c,ln=None):
    sh=s.shapes.add_shape(MSO_SHAPE.RECTANGLE,Cm(l),Cm(t),Cm(w),Cm(h))
    sh.fill.solid(); sh.fill.fore_color.rgb=c
